test("aggregation reports weighted quality, repeated-run variance, pairwise outcomes, and disagreement", () => {
  const candidates = [
    candidate("sol-high", 1),
    candidate("sol-high", 2),
    candidate("sol-high", 3),
    candidate("opus-xhigh", 1),
    candidate("opus-xhigh", 2),
    candidate("opus-xhigh", 3),
  ];
  const built = buildBlindPacket({
    candidates,
    rubric: rubric(),
    scenario: { workflow: "dev", case_id: "dev-happy-path", prompt: QUALITY_PROMPT },
    salt: "variance-secret",
  });
  const firstScores = Object.fromEntries(
    built.packet.candidates.map((item) => {
      const profile = built.key.candidates[item.id].profile_id;
      const repeat = built.key.candidates[item.id].repeat;
      return [
        item.id,
        profile === "sol-high"
          ? { judgment: 5 - (repeat - 1), clarity: 4 }
          : { judgment: 3, clarity: 3 },
      ];
    })
  );
  const secondScores = Object.fromEntries(
    Object.entries(firstScores).map(([id, scores]) => [
      id,
      { judgment: scores.judgment === 5 ? 2 : scores.judgment, clarity: scores.clarity },
    ])
  );
  const aggregate = aggregateQualityResults({
    candidates,
    packet: built.packet,
    packets: built.judgePackets,
    key: built.key,
    rubric: rubric(),
    judgments: [
      judgment(built.packet, firstScores, "judge-a"),
      judgment(built.judgePackets[1], secondScores, "judge-b"),
    ],
    minimumRepeats: 3,
  });

  assert.equal(aggregate.profiles["sol-high"].repeats, 3);
  assert.equal(aggregate.profiles["sol-high"].variance.claimable, true);
  assert.equal(typeof aggregate.profiles["sol-high"].variance.standard_deviation, "number");
  assert.equal(typeof aggregate.profiles["sol-high"].dimensions.judgment.mean, "number");
  assert.equal(aggregate.profiles["sol-high"].dimensions.judgment.coverage, 1);
  assert.equal(aggregate.profiles["sol-high"].latency.mean_ms, 2000);
  assert.equal(aggregate.adjudication_required, true);
  assert.equal(aggregate.behavioral.excluded, 0);
  assert.ok(aggregate.pairwise.total > 0);
  assert.equal(aggregate.pairwise.total, 6, "three matched pairs across two judges");
  assert.throws(
    () =>
      aggregateQualityResults({
        candidates,
        packet: built.packet,
        packets: built.judgePackets,
        key: built.key,
        rubric: rubric(),
        judgments: [
          judgment(built.packet, firstScores, "same-judge"),
          judgment(built.judgePackets[1], secondScores, "same-judge"),
        ],
      }),
    /unique judge identifiers/
  );
  const relabeled = judgment(built.packet, secondScores, "judge-c");
  relabeled.view_id = built.judgePackets[1].view_id;
  assert.throws(
    () =>
      aggregateQualityResults({
        candidates,
        packet: built.packet,
        packets: built.judgePackets,
        key: built.key,
        rubric: rubric(),
        judgments: [judgment(built.packet, firstScores, "judge-a"), relabeled],
      }),
    /unauthenticated packet view|view_sha256/
  );
});

