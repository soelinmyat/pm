test("judgments require complete rubric coverage, evidence, and complete pairwise rows", () => {
  const built = buildBlindPacket({
    candidates: [candidate("sol-high", 1), candidate("opus-xhigh", 1)],
    rubric: rubric(),
    scenario: { workflow: "dev", case_id: "dev-happy-path", prompt: QUALITY_PROMPT },
    salt: "secret",
  });
  const scores = Object.fromEntries(
    built.packet.candidates.map((item, index) => [
      item.id,
      { judgment: index === 0 ? 5 : 2, clarity: index === 0 ? 4 : 2 },
    ])
  );
  assert.equal(validateJudgment(judgment(built.packet, scores), built.packet).ok, true);

  const invalid = judgment(built.packet, scores);
  invalid.candidates[0].dimensions[0].evidence = "";
  invalid.candidates[0].extra = true;
  invalid.candidates[0].dimensions.pop();
  invalid.pairwise = [];
  const result = validateJudgment(invalid, built.packet);
  assert.equal(result.ok, false);
  assert.match(result.issues.join("\n"), /evidence is required/);
  assert.match(result.issues.join("\n"), /missing dimension clarity/);
  assert.match(result.issues.join("\n"), /missing pairwise comparison/);
  assert.match(result.issues.join("\n"), /unknown field extra/);

  const allNotApplicable = judgment(built.packet, scores);
  allNotApplicable.candidates[0].dimensions.forEach((item) => {
    item.score = "not_applicable";
  });
  assert.match(
    validateJudgment(allNotApplicable, built.packet).issues.join("\n"),
    /at least one applicable dimension/
  );
});

