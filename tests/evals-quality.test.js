function rubric() {
  return {
    $schema: "https://pm-plugin.local/evals/quality-rubric.schema.json",
    schema_version: 1,
    disagreement_threshold: 1.5,
    dimensions: [
      {
        id: "judgment",
        label: "Judgment",
        weight: 2,
        anchors: { 1: "Misses the decision.", 3: "Usable.", 5: "Excellent tradeoffs." },
      },
      {
        id: "clarity",
        label: "Clarity",
        weight: 1,
        anchors: { 1: "Confusing.", 3: "Understandable.", 5: "Effortless to use." },
      },
    ],
  };
}

function suite(overrides = {}) {
  const cases = REQUIRED_CASE_TYPES.map((type) => ({
    id: `dev-${type}`,
    type,
    prompt_ref: `evals/quality/cases/dev-${type}.md`,
    scenario_ref: `quality-dev-${type}`,
    scenario_contract_hash: "sha256:" + "a".repeat(64),
  }));
  return {
    $schema: "https://pm-plugin.local/evals/quality-suite.schema.json",
    schema_version: 1,
    rubric_ref: "evals/quality/rubric.json",
    minimum_repeats: 3,
    profiles: [
      { id: "sol-high", adapter: "codex", model: "gpt-5.6-sol", effort: "high" },
      { id: "opus-xhigh", adapter: "claude", model: "claude-opus-4-8", effort: "xhigh" },
    ],
    workflows: [{ id: "dev", authority_boundary: true, cases }],
    ...overrides,
  };
}

function candidate(profileId, repeat, overrides = {}) {
  const model = profileId === "sol-high" ? "gpt-5.6-sol" : "claude-opus-4-8";
  const content = `A useful report. Generated by ${model}.`;
  return {
    schema_version: 1,
    workflow: "dev",
    case_id: "dev-happy-path",
    case_type: "happy-path",
    release: "1.13.12",
    quality_case_hash: `sha256:${crypto.createHash("sha256").update(QUALITY_PROMPT).digest("hex")}`,
    source_hash: "sha256:" + "a".repeat(64),
    behavioral: {
      status: "pass",
      artifact_ref: `runs/20260712T01010${repeat}Z--dev-review-before-push--codex`,
      scenario_hash: "sha256:" + "b".repeat(64),
    },
    profile: {
      id: profileId,
      adapter: profileId === "sol-high" ? "codex" : "claude",
      model,
      effort: profileId === "sol-high" ? "high" : "xhigh",
    },
    runtime: { duration_ms: repeat * 1000, status: "complete" },
    repeat,
    artifacts: [
      {
        name: "report.md",
        media_type: "text/markdown",
        sha256: `sha256:${crypto.createHash("sha256").update(content).digest("hex")}`,
        content,
      },
    ],
    ...overrides,
  };
}

function judgment(packet, scoresByBlindId, judge = "judge-a") {
  const ids = packet.candidates.map((item) => item.id);
  const pairwise = packet.pairwise_plan.map(({ left, right }) => ({
    left,
    right,
    preference:
      scoresByBlindId[left].judgment > scoresByBlindId[right].judgment
        ? left
        : scoresByBlindId[left].judgment < scoresByBlindId[right].judgment
          ? right
          : "tie",
    reason: "Direct comparison of decision quality.",
  }));
  return {
    $schema: "https://pm-plugin.local/evals/quality-judgment.schema.json",
    schema_version: 1,
    packet_id: packet.packet_id,
    view_id: packet.view_id,
    view_sha256: packet.view_sha256,
    judge,
    candidates: ids.map((id) => ({
      id,
      dimensions: Object.entries(scoresByBlindId[id]).map(([dimension, score]) => ({
        dimension,
        score,
        evidence: `${dimension} evidence in the artifact`,
      })),
      summary: "Grounded assessment.",
    })),
    pairwise,
  };
}

