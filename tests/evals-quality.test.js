test("blind packets redact identities, randomize order deterministically, and exclude failures", () => {
  const sol = candidate("sol-high", 1);
  sol.artifacts[0].name = "sol-high-report.md";
  const failed = candidate("sol-high", 2, {
    behavioral: {
      status: "fail",
      artifact_ref: "runs/20260712T010102Z--dev-review-before-push--codex",
      scenario_hash: "sha256:" + "b".repeat(64),
    },
  });
  const built = buildBlindPacket({
    candidates: [sol, candidate("opus-xhigh", 1), failed],
    rubric: rubric(),
    scenario: { workflow: "dev", case_id: "dev-happy-path", prompt: QUALITY_PROMPT },
    salt: "test-only-secret",
  });

  assert.equal(built.packet.candidates.length, 2);
  assert.equal(built.excluded.length, 1);
  assert.equal(built.excluded[0].reason, "behavioral-fail");
  const serialized = JSON.stringify(built.packet);
  for (const identity of ["sol-high", "opus-xhigh", "gpt-5.6", "claude", "codex"]) {
    assert.doesNotMatch(serialized.toLowerCase(), new RegExp(identity.replace(".", "\\.")));
  }
  assert.match(serialized, /\[redacted-model-identity\]/);
  assert.equal(Object.keys(built.key.candidates).length, 2);
  assert.equal(built.packet.pairwise_plan.length, 1);
  assert.equal(validateBlindPacket(built.packet).ok, true);
  assert.deepEqual(
    built.judgePackets[1].candidates.map((item) => item.id),
    [...built.packet.candidates.map((item) => item.id)].reverse()
  );
  assert.doesNotMatch(JSON.stringify(built.packet).toLowerCase(), /sol-high-report/);

  built.packet.candidates[0].artifacts[0].content += " tampered";
  assert.match(
    validateBlindPacket(built.packet).issues.join("\n"),
    /sha256 does not match content/
  );
});

