test("quality scorecard comparison reports release and dimension deltas", () => {
  const identity = {
    source_hash: "sha256:" + "a".repeat(64),
    scenario_hash: "sha256:" + "b".repeat(64),
    quality_case_hash: "sha256:" + "c".repeat(64),
    rubric_hash: "sha256:" + "d".repeat(64),
    evaluation_design_hash: "sha256:" + "e".repeat(64),
  };
  const baseline = {
    workflow: "dev",
    case_id: "dev-happy-path",
    eligible_candidates: 6,
    behavioral_failures: 0,
    profiles: {
      "sol-high": { mean: 3.5, dimensions: { judgment: { mean: 3.2 } } },
    },
    quality_winner: "opus-xhigh",
    evaluation_identity: identity,
    judges: ["judge-a"],
  };
  const current = {
    ...baseline,
    profiles: {
      "sol-high": { mean: 4.1, dimensions: { judgment: { mean: 4.0 } } },
    },
    quality_winner: "sol-high",
    judges: ["judge-a", "judge-b"],
  };
  const result = compareQualityScorecards(baseline, current);
  assert.equal(result.comparable, true);
  assert.equal(result.profiles["sol-high"].mean_delta, 0.6);
  assert.equal(result.profiles["sol-high"].dimensions.judgment, 0.8);
  assert.equal(result.winner_changed, true);
  assert.equal(result.judge_count_delta, 1);

  current.evaluation_identity = { ...identity, rubric_hash: "sha256:" + "f".repeat(64) };
  assert.equal(compareQualityScorecards(baseline, current).reason, "rubric-mismatch");
});
